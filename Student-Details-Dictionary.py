# Python Program: Student Details Entry Using Dictionary
# Creating an empty dictionary
Student_details = {}
# Collecting student information
Student_details['First Name'] = input("Enter First Name: ")
Student_details['Last Name'] = input("Enter Last Name: ")
Student_details['Gender'] = input("Enter Gender: ")
Student_details['Contact Number'] = input("Enter Contact Number: ")
Student_details['Date of Birth'] = input("Enter Date of Birth (DD/MM/YYYY): ")
Student_details['Highest Qualification'] = input("Enter Highest Qualification: ")
Student_details['Current Ongoing Degree'] = input("Enter Current Ongoing Degree: ")
Student_details['Mail ID'] = input("Enter Mail ID: ")
Student_details['Password'] = input("Enter Password: ")
# Displaying the collected student details
print("\nStudent Details Collected:")
print(Student_details)
